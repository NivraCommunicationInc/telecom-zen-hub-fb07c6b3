// Helper to check if email is valid (not placeholder)
const isValidEmail = (email: string | null | undefined): boolean => {
  if (!email) return false;
  const invalidPatterns = [
    /^x@/i, /^noemail@/i, /^no\.email@/i, /^nomail@/i, /^email@telus/i,
    /^noemial@/i, /^normail@/i, /^no2mail@/i, /^xemail@/i, /^moemail@/i,
    /^x\.@/i, /^\.@/i, /^kersten@master\.ca$/i, /^no\.email\.ttelus@/i,
    /^nomailtelus@/i, /^no\.emailkoodo@/i, /^xxxxx@/i, /^nomail@koodo/i
  ];
  for (const pattern of invalidPatterns) {
    if (pattern.test(email)) return false;
  }
  const emailRegex = /^[A-Z0-9._%+-]+@[A-Z0-9.-]+\.[A-Z]{2,}$/i;
  return emailRegex.test(email);
};

